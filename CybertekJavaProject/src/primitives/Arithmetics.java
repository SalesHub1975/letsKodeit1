package primitives;

public class Arithmetics {

	public static void main(String[] args) {
		int a = 5;
		int b = 3;
		
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a%b);
		System.out.println(a/b);
		
		System.out.println("                                               ");
			
		// a / b gives us 1 --> but if change int to double w'll have little diffrent result
		
		double x = 5;
		double y = 3;
		
		System.out.println(x+y
				);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x%y);
		System.out.println(x/y);
	}
}

